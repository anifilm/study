<?php

$a = 5; // $a에 5를 대입
echo $a.'<br>';

$a += 3; // $a = $a + 3 와 동일
echo $a.'<br>';

$a -= 4; // $a = $a - 4 와 동일
echo $a.'<br>';

$a *= 2; // $a = $a * 2 와 동일
echo $a.'<br>';

$a /= 4; // $a = $a / 4 와 동일
echo $a.'<br>';

$a %= 2; // $a = $a % 2 와 동일
echo $a.'<br>';

$a = '딸기';
$a .= ' 주스'; // $a = $a.' 주스' 와 동일 (문자열 연결)
echo $a.'<br>';
