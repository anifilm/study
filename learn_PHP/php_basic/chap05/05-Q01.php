<?php
/* 01
다음은 배열을 이용하여 어떤 과목에 대한 학생 10명의 점수 합계와 평균을 구하는 프로그램이다. 빈칸을 채워
프로그램을 완성하시오.
(출력 포맷, 예제 코드 생략...)
 */

$score = [87, 76, 98, 87, 87, 93, 79, 85, 88, 63];

$sum = 0;

for ($i = 0; $i < count($score); $i++) {
  $sum += $score[$i]; // 학생 10명의 점수 누적함
}

$avg = $sum / count($score); // 평균 구하기
echo '입력된 점수: ';

foreach ($score as $s) {
  echo $s.' '; // 입력된 학생의 점수를 출력
}
echo '<br>';

echo "합계: $sum, 평균: $avg";
