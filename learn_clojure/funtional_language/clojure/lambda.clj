; 람다 함수
; 이름이 없는 함수
; 클로저에서는 람다 함수를 정의할 때 fn 키워드를 사용한다.

(defn sum [x y] (+ x y)) ; 클로저 함수

(fn [x y] (+ x y)) ; 람다 함수

((fn [x y] (+ x y)) 1 2)
; 3

; 람다 함수를 변후에 저장하는 예
(def f (fn [x] (+ x 1)))

(f 1)
; 2

; 람다 함수를 반환하는 함수의 예
(defn f [x] (fn [y] (+ x y)))

((f 1) 2)
; 3

; 함수를 인자로 받아들이는 함수의 예
(defn f [fp] (fp 1))

(f (fn [x] (+ x 1)))
; 2
